import config.settings as settings

def format_profile(user: dict, show_contact: bool = False) -> str:
    if not user:
        return "‚ùå –ü—Ä–æ—Ñ–∏–ª—å –Ω–µ –Ω–∞–π–¥–µ–Ω"

    game = user.get('current_game') or user.get('game', 'dota')

    text = f"üë§ {user['name']}\n"
    text += f"üéÆ {user['nickname']}\n"
    text += f"üéÇ {user['age']} –ª–µ—Ç\n"

    rating = user['rating']
    if rating == 'any':
        text += f"üèÜ –õ—é–±–æ–π —Ä–µ–π—Ç–∏–Ω–≥\n"
    elif rating in settings.RATINGS.get(game, {}):
        rating = settings.RATINGS[game][rating]
        text += f"üèÜ {rating}\n"
    else:
        text += f"üèÜ {rating}\n"

    region = user.get('region', '')
    if region == 'any':
        text += f"üåç –õ—é–±–æ–π —Ä–µ–≥–∏–æ–Ω\n"
    elif region and region in settings.REGIONS:
        text += f"üåç {settings.REGIONS[region]}\n"

    if user['positions']:
        if 'any' in user['positions']:
            text += f"‚öîÔ∏è –õ—é–±–∞—è –ø–æ–∑–∏—Ü–∏—è\n"
        else:
            positions_text = []
            for pos in user['positions']:
                if pos in settings.POSITIONS.get(game, {}):
                    positions_text.append(settings.POSITIONS[game][pos])
                else:
                    positions_text.append(pos)
            text += f"‚öîÔ∏è {', '.join(positions_text)}\n"

    if user.get('additional_info'):
        text += f"\nüìù {user['additional_info']}\n"

    if show_contact and user.get('username'):
        text += f"\nüí¨ @{user['username']}"

    return text

